{
	size_t idx;

	if ((idx = mb_ucsbsearch(data_wcrange_tolower,
	    NELEM(data_wcrange_tolower), c)) == (size_t)-1)
		return (c);
	return (c + data_wcdelta_tolower[idx]);
}

